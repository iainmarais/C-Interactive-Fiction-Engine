namespace InteractiveFiction_CLI

{

    public static class StringExtensions
    {
        public static string IsNotBlank(this string source)
        {
            //set null condition to null in output to reduce console spam, null is handled appropriately in the CommandProcessor.cs file
            return (string.IsNullOrEmpty(source) ? null : source);
        }

        //This func capitalises the first letter of each string/word entered, for consistency.
        public static string CapitaliseFirstLetter(string value)
        {
            value = value.ToLower();
            char[] array = value.ToCharArray();
            if (array.Length >= 1)
            {
                if (char.IsLower(array[0]))
                {
                    array[0] = char.ToUpper(array[0]);
                }
            }
            for (int i = 1; i < array.Length; i++)
            {
                if (array[i - 1] == ' ')
                {
                    if (char.IsLower(array[i]))
                    {
                        array[i] = char.ToUpper(array[i]);
                    }
                }
            }
            return new string(array);
        }
    }
}

